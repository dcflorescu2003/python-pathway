export type ExerciseType = "quiz" | "fill" | "order" | "truefalse";

export interface ExerciseOption {
  id: string;
  text: string;
}

export interface Exercise {
  id: string;
  type: ExerciseType;
  question: string;
  // quiz
  options?: ExerciseOption[];
  correctOptionId?: string;
  // fill
  codeTemplate?: string;
  blanks?: { id: string; answer: string }[];
  // order
  lines?: { id: string; text: string; order: number }[];
  // truefalse
  statement?: string;
  isTrue?: boolean;
  explanation?: string;
  xp: number;
}

export interface Lesson {
  id: string;
  title: string;
  description: string;
  exercises: Exercise[];
  xpReward: number;
}

export interface Chapter {
  id: string;
  number: number;
  title: string;
  description: string;
  icon: string;
  color: string;
  lessons: Lesson[];
}

